package DTO;

public class ComponentDTO {
	private int component_num;
	private String component_name;
	private String component_count;
	private String component_cost;
	private int shop_num;
	private String shop_name;
	
	public int getComponent_num() {
		return component_num;
	}
	public void setComponent_num(int component_num) {
		this.component_num = component_num;
	}
	public String getComponent_name() {
		return component_name;
	}
	public void setComponent_name(String component_name) {
		this.component_name = component_name;
	}
	public String getComponent_count() {
		return component_count;
	}
	public void setComponent_count(String component_count) {
		this.component_count = component_count;
	}
	public String getComponent_cost() {
		return component_cost;
	}
	public void setComponent_cost(String component_cost) {
		this.component_cost = component_cost;
	}
	public int getShop_num() {
		return shop_num;
	}
	public void setShop_num(int shop_num) {
		this.shop_num = shop_num;
	}
	public String getShop_name() {
		return shop_name;
	}
	public void setShop_name(String shop_name) {
		this.shop_name = shop_name;
	}
	
	
	
}
