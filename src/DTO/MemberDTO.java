package DTO;

public class MemberDTO {
	private int member_num;
	private String member_id;
	private String member_pw;
	private String member_phone;
	private String member_name;
	private String member_car;
	private String member_carNum;
	
	public int getMember_num() {
		return member_num;
	}
	public void setMember_num(int member_num) {
		this.member_num = member_num;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getMember_pw() {
		return member_pw;
	}
	public void setMember_pw(String member_pw) {
		this.member_pw = member_pw;
	}
	public String getMember_phone() {
		return member_phone;
	}
	public void setMember_phone(String member_phone) {
		this.member_phone = member_phone;
	}
	public String getMember_name() {
		return member_name;
	}
	public void setMember_name(String member_name) {
		this.member_name = member_name;
	}
	public String getMember_car() {
		return member_car;
	}
	public void setMember_car(String member_car) {
		this.member_car = member_car;
	}
	public String getMember_carNum() {
		return member_carNum;
	}
	public void setMember_carNum(String member_carNum) {
		this.member_carNum = member_carNum;
	}

	

}
